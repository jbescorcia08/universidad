    public void refrescarTabla() {
        // borrar todos los elementos vacio de modelo
        while(modelo.getRowCount() > 0){
            modelo.removeRow(0);
        }
        for (Alumno l : listaAlumnos) {
            Object []obj = new Object[5];
            obj[0] = l.getNonbre();
            obj[1] = l.getCarrera();
            obj[2] = l.getGrupo();
            obj[3] = l.getSemestre();
            obj[4] = l.getPromedio();
            
            modelo.addRow(obj);
        }
        // se llama el nombre de la tabla y se envia el modelo ya registrado
        registoAlumnos.setModel(modelo);
    }

